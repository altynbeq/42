#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	check;

	check = 0;
	while (*str != '\0')
	{
		check++;
		if (!('a' <= *str && *str <= 'z'))
		{
			return (0);
		}
		str++;
	}
	if (check == 0)
	{
		return (1);
	}
	return (1);
}
